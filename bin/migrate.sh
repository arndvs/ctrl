#!/usr/bin/env bash
# migrate.sh — Read-only diagnostic for existing Claude Code setups.
#
# Usage:
#   bash ~/dotfiles/bin/migrate.sh
#
# Nothing is written to managed config locations.
# Generates a report under working/ for review.

set -euo pipefail

DOTFILES="$HOME/dotfiles"
CLAUDE_DIR="$HOME/.claude"
REPORT_DIR="$DOTFILES/working/research"
REPORT_FILE="$REPORT_DIR/migrate-report.md"

red()    { printf '\033[0;31m  %s\033[0m\n' "$*"; }
green()  { printf '\033[0;32m  %s\033[0m\n' "$*"; }
yellow() { printf '\033[0;33m  %s\033[0m\n' "$*"; }
dim()    { printf '\033[2m  %s\033[0m\n' "$*"; }
header() { printf '\n\033[1m%s\033[0m\n' "$*"; printf '%s\n' "$(printf '─%.0s' $(seq 1 60))"; }

REPORT=()
report() { REPORT+=("$1"); }

echo ""
echo "  ctrl+shft migration diagnostic"
echo "  ─────────────────────────────────────────────────────────"
echo "  Read-only. Nothing will be written or changed."
echo "  A full report will be saved to: $REPORT_FILE"
echo ""

report "# ctrl+shft Migration Report"
report ""
report "> Generated by \`migrate.sh\` on $(date '+%Y-%m-%d %H:%M')"
report "> Read-only scan — nothing was changed."
report ""

header "[1/5] Existing ~/.claude/ inventory"
report "## 1. Existing ~/.claude/ inventory"
report ""

if [[ ! -d "$CLAUDE_DIR" ]]; then
    yellow "~/.claude/ does not exist — clean slate, no conflicts"
    report "~/.claude/ does not exist. Clean slate — bootstrap will create it."
    report ""
else
    green "~/.claude/ found"
    report "### Contents"
    report ""
    report "\`\`\`"

    for item in CLAUDE.md skills agents rules; do
        path="$CLAUDE_DIR/$item"
        if [[ -L "$path" ]]; then
            _target=$(readlink "$path")
            yellow "  $path → symlink → $_target"
            report "  $path → symlink → $_target"
        elif [[ -f "$path" ]] || [[ -d "$path" ]]; then
            if [[ -f "$path" ]]; then
                _info="$(wc -l < "$path") lines"
            else
                _info="directory"
            fi
            green "  $path — real file/dir ($_info)"
            report "  $path — real ($_info)"
        else
            dim "  $path — not found"
            report "  $path — not found"
        fi
    done

    report "\`\`\`"
    report ""
fi

header "[2/5] Conflict analysis"
report "## 2. Conflict analysis"
report ""

_conflicts=0
_clean=0

check_conflict() {
    local path="$1"
    local label="$2"
    local target="$3"

    if [[ -L "$path" ]]; then
        local existing_target
        existing_target=$(readlink "$path")
        if [[ "$existing_target" == "$target" ]]; then
            green "  $label — already symlinked correctly"
            report "- ✓ \`$label\` — already correct, no action needed"
            (( _clean++ )) || true
        else
            yellow "  $label — symlink exists pointing elsewhere"
            yellow "    current → $existing_target"
            yellow "    --adopt → $target"
            report "- ⚠ \`$label\` — symlink exists → \`$existing_target\`"
            report "  - \`--adopt\` will repoint to \`$target\`"
            (( _conflicts++ )) || true
        fi
    elif [[ -e "$path" ]]; then
        red "  $label — real file/directory exists"
        red "    --adopt will back up to ${path}.pre-ctrlshft"
        report "- ✗ \`$label\` — real file/directory exists"
        report "  - \`--adopt\` backs up to \`${path}.pre-ctrlshft\` then replaces with symlink"
        (( _conflicts++ )) || true
    else
        green "  $label — not found, will be created cleanly"
        report "- ✓ \`$label\` — not found, will be created"
        (( _clean++ )) || true
    fi
}

check_conflict "$CLAUDE_DIR/CLAUDE.md" "~/.claude/CLAUDE.md" "$DOTFILES/CLAUDE.md"
check_conflict "$CLAUDE_DIR/skills" "~/.claude/skills" "$DOTFILES/skills"
check_conflict "$CLAUDE_DIR/agents" "~/.claude/agents" "$DOTFILES/agents"
check_conflict "$CLAUDE_DIR/rules" "~/.claude/rules" "$DOTFILES/rules"

report ""
report "**Summary:** $_conflicts conflict(s), $_clean clean"
report ""
echo ""
echo "  Conflicts: $_conflicts   Clean: $_clean"

header "[3/5] CLAUDE.md content analysis"
report "## 3. CLAUDE.md content analysis"
report ""

_existing_claude=""
if [[ -f "$CLAUDE_DIR/CLAUDE.md" ]] && [[ ! -L "$CLAUDE_DIR/CLAUDE.md" ]]; then
    _existing_claude="$CLAUDE_DIR/CLAUDE.md"
elif [[ -L "$CLAUDE_DIR/CLAUDE.md" ]]; then
    _target=$(readlink "$CLAUDE_DIR/CLAUDE.md")
    [[ -f "$_target" ]] && _existing_claude="$_target"
fi

if [[ -n "$_existing_claude" ]] && [[ -f "$_existing_claude" ]]; then
    _lines=$(wc -l < "$_existing_claude")
    _words=$(wc -w < "$_existing_claude")
    green "  Found CLAUDE.md: $_lines lines, $_words words"
    report "### Potential skill candidates"
    report ""

    _skill_candidates=0
    _current_section=""
    _seen_sections=()

    while IFS= read -r line; do
        if [[ "$line" =~ ^#{1,3}[[:space:]] ]]; then
            _current_section="$line"
        fi
        if [[ "$line" =~ ^[0-9]+\.[[:space:]] ]] || [[ "$line" =~ ^\`\`\` ]] || [[ "$line" =~ (always|never|when you|step [0-9]|checklist|procedure|workflow) ]]; then
            if [[ -n "$_current_section" ]]; then
                _already_seen=false
                for s in "${_seen_sections[@]:-}"; do
                    [[ "$s" == "$_current_section" ]] && _already_seen=true
                done
                if [[ "$_already_seen" == false ]]; then
                    yellow "    → $_current_section"
                    report "- $_current_section"
                    _seen_sections+=("$_current_section")
                    (( _skill_candidates++ )) || true
                fi
            fi
        fi
    done < "$_existing_claude"

    if [[ "$_skill_candidates" -eq 0 ]]; then
        dim "  No strong skill candidates — CLAUDE.md looks mostly like context"
        report "No strong skill candidates detected."
    fi
else
    dim "  No existing CLAUDE.md to analyze"
    report "No existing CLAUDE.md found."
fi
report ""

header "[4/5] Project CLAUDE.md scan"
report "## 4. Project CLAUDE.md scan"
report ""

_search_dirs=()
for d in "$HOME/projects" "$HOME/code" "$HOME/dev" "$HOME/work" "$HOME/src" "$HOME/Sites"; do
    [[ -d "$d" ]] && _search_dirs+=("$d")
done

_project_claudes=()
if [[ ${#_search_dirs[@]} -gt 0 ]]; then
    while IFS= read -r f; do
        [[ "$f" == "$CLAUDE_DIR/CLAUDE.md" ]] && continue
        [[ "$f" == "$DOTFILES/CLAUDE.md" ]] && continue
        [[ "$f" == "$DOTFILES/CLAUDE.base.md" ]] && continue
        _project_claudes+=("$f")
    done < <(find "${_search_dirs[@]}" -maxdepth 3 -name "CLAUDE.md" -not -path "*/node_modules/*" -not -path "*/.git/*" -not -path "*/dotfiles/*" 2>/dev/null | sort)
fi

if [[ ${#_project_claudes[@]} -eq 0 ]]; then
    dim "  No project CLAUDE.md files found"
    report "No project CLAUDE.md files found in common locations."
else
    green "  Found ${#_project_claudes[@]} project CLAUDE.md file(s)"
    report "Found **${#_project_claudes[@]}** project CLAUDE.md file(s):"
    report ""
    for f in "${_project_claudes[@]}"; do
        _lines=$(wc -l < "$f")
        report "- \`$f\` — $_lines lines"
    done
fi
report ""

header "[5/5] Shell integration check"
report "## 5. Shell integration check"
report ""

_MARKER="# ── dotfiles/load-secrets ──"

check_shell_rc() {
    local rc="$1"
    local name="$2"
    if [[ ! -f "$rc" ]]; then
        dim "  $name — not found"
        report "- \`$name\` — not found"
        return
    fi
    if grep -qF "$_MARKER" "$rc"; then
        yellow "  $name — ctrl+shft snippet already present"
        report "- \`$name\` — ctrl+shft snippet already present"
    else
        green "  $name — will have snippet appended by bootstrap"
        report "- \`$name\` — snippet not present, bootstrap will append it"
    fi
}

check_shell_rc "$HOME/.bashrc" "~/.bashrc"
check_shell_rc "$HOME/.zshrc" "~/.zshrc"
report ""

header "Summary"
report "## Recommended next step"
report ""

if [[ "$_conflicts" -eq 0 ]]; then
    green "  Clean migration — no conflicts detected"
    echo ""
    echo "  Run:  bash ~/dotfiles/bin/bootstrap.sh"
    report "**Clean slate** — no conflicts. Run:"
    report "\`\`\`bash"
    report "bash ~/dotfiles/bin/bootstrap.sh"
    report "\`\`\`"
else
    yellow "  $_conflicts conflict(s) — adopt mode recommended"
    echo ""
    echo "  Run:  bash ~/dotfiles/bin/bootstrap.sh --adopt"
    report "**$_conflicts conflict(s) detected** — use adopt mode:"
    report "\`\`\`bash"
    report "bash ~/dotfiles/bin/bootstrap.sh --adopt"
    report "\`\`\`"
fi

report ""
report "---"
report "*Re-run \`migrate.sh\` anytime to refresh this report.*"

mkdir -p "$REPORT_DIR"
printf '%s\n' "${REPORT[@]}" > "$REPORT_FILE"

green "  Report saved: $REPORT_FILE"
